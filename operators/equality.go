// Equality Operators

package main

import "fmt"

func operators2() {
	a := 14
	b := 30

	// '==' (equals)
	result1 := a == b
	fmt.Println(result1)

	// '!=' (not equals)
	result2 := a != b
	fmt.Println(result2)

	// '<' (less than)
	result3 := a < b
	fmt.Println(result3)

	// '>' (greater than)
	result4 := a > b
	fmt.Println(result4)

	// '<=' (less than or equal to)
	result5 := a <= b
	fmt.Println(result5)

	// '>=' (greater than or equal to)
	result6 := a >= b
	fmt.Println(result6)

}
